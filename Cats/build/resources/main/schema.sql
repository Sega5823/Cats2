CREATE TABLE IF NOT EXISTS cats
(
    name character varying(255) COLLATE "default",
    breed character varying(255) COLLATE "default",
    color character varying(255) COLLATE "default",
    date date,
    catid integer NOT NULL GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 2147483647 CACHE 1 ),
    owner_id integer,
    CONSTRAINT cats_pkey PRIMARY KEY (catid)
    );

CREATE TABLE IF NOT EXISTS friendship
(
    catid integer NOT NULL,
    friendid integer NOT NULL,
    CONSTRAINT friendship_pkey PRIMARY KEY (catid, friendid)
    );

