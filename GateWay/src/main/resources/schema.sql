CREATE TABLE IF NOT EXISTS tokens
(
    user_id integer,
    date date,
    token character varying COLLATE "default",
    token_id integer NOT NULL GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 2147483647 CACHE 1 ),
    CONSTRAINT tokens_pkey PRIMARY KEY (token_id)
    );

CREATE TABLE IF NOT EXISTS users
(
    id integer NOT NULL GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 2147483647 CACHE 1 ),
    username character varying COLLATE "default",
    password character varying COLLATE "default",
    role character varying COLLATE "default",
    email character varying COLLATE "default",
    CONSTRAINT users_pkey PRIMARY KEY (id)
    );